def write_json(path: Path, data: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    wrapped = {k: {"message": v} for k, v in data.items()}
    path.write_text(json.dumps(wrapped, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def merge_code_json(locale: str, translations: dict) -> None:
    en_path = I18N / "en" / "code.json"
    out_path = I18N / locale / "code.json"
    if en_path.exists():
        with open(en_path, encoding="utf-8") as f:
            base = json.load(f)
    else:
        base = {}
    for key, msg in translations.items():
        base[key] = {"message": msg}
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(base, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def main() -> None:
    for locale, t in TRANSLATIONS.items():
        code = {**t["code"], **API_UI_EXTRA.get(locale, {})}
        write_json(I18N / locale / "docusaurus-theme-classic" / "navbar.json", t["navbar"])
        write_json(I18N / locale / "docusaurus-theme-classic" / "footer.json", t["footer"])
        write_json(I18N / locale / "docusaurus-plugin-content-docs" / "current.json", t["sidebar"])
        merge_code_json(locale, code)
        print(f"✓ {locale}")

    print("Writing localized overview pages...")
    import subprocess
    subprocess.run([sys.executable, str(ROOT / "scripts" / "write_doc_pages.py")], check=True)
    print("Done.")


if __name__ == "__main__":
    main()
